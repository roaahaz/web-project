      <h3><b>HISTORY</b></h3>
    </div>

    <div class="w3-container">
      <p><h6>
         Revelations – the Diane Arbus retrospective organized by the San Francisco Museum of Modern Art in 2003 – inspired the purchase of the Pilara Foundation’s first photograph: a portrait from Arbus' challenging and emotive Untitled series. The emotional intensity characterizing this photograph has informed subsequent acquisitions for the collection, which now exceeds over 4,000 works by international artists that span the history of the medium and its international breadth. At the collection’s core are those photographers first exhibited in two seminal twentieth century exhibitions: New Documents (1967) at the Museum of Modern Art and New Topographics (1975) at Eastman House. In recent years, the Foundation has collected emerging photographers in depth, developing holdings that speak to evolving practices within the medium.

          Since opening to the public in 2010, Pier 24 Photography has produced eleven exhibitions. The inaugural exhibition highlighted prominent veins running through the collection with rooms dedicated to portraiture, contemporary Bay Area photographers, historical images of San Francisco, early American color photography, and works produced through the Farm Security Administration (FSA). That exhibition also included complete portfolios by Diane Arbus, Larry Clark, Lee Friedlander, and Garry Winogrand. In many of the exhibitions that have followed, these themes have been explored in greater depth.</p></h6>

</b>
<b>
 
    <div class="w3-container">
      <h3><b>BUILDING</b></h3>
    </div>

    <div class="w3-container">
      <p><h6>The Pilara Foundation is the first to inhabit this historic space in nearly three decades. After two years spent remodeling the building, Pier 24 Photography opened to the public in 2010. The building’s design straddles the line between storage and exhibition space, while maintaining a gallery-like aesthetic. The scale of the space allows for the Foundation’s collection to be displayed with few restrictions, and the location off the Embarcadero promenade, directly underneath the Bay Bridge, provides for a breathtaking panoramic view of the Bay. Pier 24 respects the original use and intention of the building, pays homage to a remarkable collection of photography, and highlights the Bay Area’s stunning landscape. </p></h6>

